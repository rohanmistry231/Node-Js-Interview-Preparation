// Main Learning Points: Node.js CLI and REPL
// Node.js CLI allows running JavaScript files; REPL provides an interactive shell for executing code.

// This file contains instructions for using the Node.js CLI and REPL.

// 1. Node.js CLI (Command Line Interface)
// To run a JavaScript file using the Node.js CLI:
// - Save a file, e.g., script.js, with some code like:
// console.log('Hello from Node.js CLI!');
// - Open a terminal and navigate to the file's directory
// - Run: node script.js
// - Output: Hello from Node.js CLI!

// 2. Node.js REPL (Read-Eval-Print Loop)
// To start the REPL:
// - Open a terminal
// - Run: node
// - You'll enter an interactive shell where you can type JavaScript code
// - Example commands in REPL:
// > 1 + 2
// 3
// > const greet = () => console.log('Hello from REPL!');
// > greet()
// Hello from REPL!
// - To exit the REPL, type: .exit or press Ctrl+C twice

